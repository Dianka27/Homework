package lesson6;

// Класс Животные
public class Animal {
    // Живтоное умеет бегать
    public void run(int distance) {
        System.out.println("Животное пробежало " + distance + " м.");
    }

    // Живтоное умеет плавать
    public void swim(int distance) {
        System.out.println("Животное проплыло " + distance + " м.");
    }
}
